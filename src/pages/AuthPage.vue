export default {
    name: "AuthPage",
    data() {
        return {
            isLogin: true,
            isLoading: false,
            userData: {
                name: '',
                email: '',
                password: '',
            },
        }
    },
    methods: {
        redirect(){
            this.$router.push({path:"/"});
        },
        resetData(){
            this.userData.email = this.userData.name =this.userData.password = "";
        },
        async doLogin() {
            this.isLoading = true;
            try {
                await this.$store.dispatch("user/doLogin",{
                    email:this.userData.email,
                    password:this.userData.password,
                });
                this.$toast.success("Logged in!",{
                        position: "top-center",
                        timeout: 5000,
                        closeOnClick: true,
                        pauseOnFocusLoss: true,
                        pauseOnHover: true,
                        draggable: true,
                        draggablePercent: 0.6,
                        showCloseButtonOnHover: false,
                        hideProgressBar: true,
                        closeButton: "button",
                        icon: true,
                        rtl: false
                        });
                this.resetData();
                this.redirect();
            } catch (error) {
                console.error(error.message);
                this.$toast.error(error.message,{
                        position: "top-center",
                        timeout: 5000,
                        closeOnClick: true,
                        pauseOnFocusLoss: true,
                        pauseOnHover: true,
                        draggable: true,
                        draggablePercent: 0.6,
                        showCloseButtonOnHover: false,
                        hideProgressBar: true,
                        closeButton: "button",
                        icon: true,
                        rtl: false
                        });
            }finally{
                this.isLoading = false;
            }
        },
        async doRegister() {
            this.isLoading = true;
            try {
                await this.$store.dispatch("user/doRegister",{
                    name:this.userData.name,
                    email:this.userData.email,
                    password:this.userData.password,
                });
                this.$toast.success("Account created!",{
                        position: "top-center",
                        timeout: 5000,
                        closeOnClick: true,
                        pauseOnFocusLoss: true,
                        pauseOnHover: true,
                        draggable: true,
                        draggablePercent: 0.6,
                        showCloseButtonOnHover: false,
                        hideProgressBar: true,
                        closeButton: "button",
                        icon: true,
                        rtl: false
                        });
                this.resetData();
                this.redirect();
            } catch (error) {
                this.$toast.error(error.message,{
                        position: "top-center",
                        timeout: 5000,
                        closeOnClick: true,
                        pauseOnFocusLoss: true,
                        pauseOnHover: true,
                        draggable: true,
